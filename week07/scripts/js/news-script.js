function displayNews() {
    const newsContainer = document.getElementById('news-container');
    
    if (!newsContainer) {
        console.error('Elemento #news-container não encontrado!');
        return;
    }

    // Limpa o conteúdo atual
    newsContainer.innerHTML = ''; 

    if (newsData.articles && newsData.articles.length > 0) {
        newsData.articles.forEach(article => {
            const articleElement = document.createElement('div');
            articleElement.classList.add('news-article');
            
            const imageUrl = article.image || 'https://via.placeholder.com/400x200';

            articleElement.innerHTML = `
                <a href="${article.url}" target="_blank">
                    <img src="${imageUrl}" alt="${article.title}">
                </a>
                <h3><a href="${article.url}" target="_blank">${article.title}</a></h3>
                <p>${article.description || 'Descrição não disponível.'}</p>
                <p><strong>Fonte:</strong> ${article.source.name}</p>
            `;
            
            newsContainer.appendChild(articleElement);
        });
    } else {
        newsContainer.innerHTML = '<p>Nenhum artigo encontrado.</p>';
    }
}

// Chama a função para exibir as notícias quando a página é carregada
window.addEventListener('DOMContentLoaded', displayNews);